//Objetos

//neste caso temos a maneira tradicional mais verbosa
//cada variável com valor

let variavel = "valor";
let variavel11 = "valor 1";
let variavel22 = "valor 2";
let variavel33 = "valor 3";
console.log("Imrimindo as variáveis :");
console.log(variavel11);
console.log(variavel22);
console.log(variavel33);

//neste caso temos um objeto

let variavelTotal = {
  //isso é chamado de keyValuePar
  variavel1: "valor 1",
  variavel2: "valor 2",
  variavel3: "valor 3",
};
console.log("Imrimindo o objeto :");
console.log(variavelTotal);

//temos um objeto chamado variável com várias propriedades

//Assim posso utilizar varias propriedades de uma mesma variável em lugares diferentes
console.log("Imrimindo itens do objeto separadamente:");
console.log(variavelTotal.variavel1);
console.log(variavelTotal.variavel2);
console.log(variavelTotal.variavel3);

//Com a criação de objetos o código fica menos verboso e mais limpo

