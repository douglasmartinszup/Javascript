//Tipos de variáveis

//Var permite redefinir valor
var doug = "doug";

//Let permite redefinir valor
let dougC = "dougC";

//Const não aceita redefinir valores
const dougCaval = "dougCaval";

//Exibindo estas variáveis
//O modo como elas são chamadas dentro de console.log é a mesma, pelo nome.

//chamando a var
console.log(doug);
//chamando a let
console.log(dougC);
//chamando a const
console.log(dougCaval);

