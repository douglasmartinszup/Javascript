//Operadores aritimeticos 


//  +(soma)
let um = 1;
let dois = 2;
let soma = um + dois ;
console.log(soma);//deve retornar a soma que é três

//  -(subtração)
let tres = 3;
let quatro = 4;
let subtracao = quatro - tres;
console.log(subtracao);//deve retornar a subtracao que é um

//  *(multiplicação)
let cinco = 5;
let seis = 6;
let multiplicacao = cinco * seis;
console.log(multiplicacao);//deve retornar a multiplicação que é 30

//  /(divisão)
let quarenta = 40;
let oito = 8;
let divisao = quarenta / oito;
console.log(divisao);//deve retornar o valor da divisao que é 5

//  **(exponencial)
let nove = 9;
let exponencial = nove * nove;
console.log(exponencial);//deve retornar o valor exponencial que é 81


