//Operadores de igualdade
//Igualdade restrita
//comparando o valorese tipo

console.log("Comparando valores e tipos");
//eu uso três sinais de igual para comparar valor e tipo
//mesmo valor  e tipo retorna true
console.log(1 === 1);

//mesmo valor e tipo diferente retorna false
console.log("1" === 1);

//comparando apenas o valor
//eu uso dois sinais de igual para comparar apenas o valor
console.log("Comparando valores");
//mesmo valor e tipo retorna true
console.log(1 == 1);
//mesmo valor e tipo diferente também retonra true
console.log("1" == 1);

